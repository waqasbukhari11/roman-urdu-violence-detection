def build_lstm_model(vocab_size, max_sequence_length, embedding_dim, lstm_units, dropout_rate, recurrent_dropout_rate, num_classes, learning_rate=0.001):
    """
    Builds an LSTM model with specified hyperparameters.

    Args:
        vocab_size (int): The size of the vocabulary.
        max_sequence_length (int): The maximum length of input sequences.
        embedding_dim (int): The dimension of word embeddings.
        lstm_units (int): The number of LSTM units.
        dropout_rate (float): The dropout rate for SpatialDropout1D and LSTM output.
        recurrent_dropout_rate (float): The recurrent dropout rate for the LSTM layer.
        num_classes (int): The number of output classes.
        learning_rate (float): The learning rate for the optimizer.

    Returns:
        tensorflow.keras.models.Sequential: The compiled LSTM model.
    """
    model = Sequential()
    model.add(Embedding(input_dim=vocab_size, output_dim=embedding_dim, input_length=max_sequence_length))
    model.add(SpatialDropout1D(dropout_rate))
    model.add(LSTM(lstm_units, dropout=dropout_rate, recurrent_dropout=recurrent_dropout_rate))
    model.add(Dense(num_classes, activation='softmax'))

    optimizer = Adam(learning_rate=learning_rate)
    model.compile(loss='sparse_categorical_crossentropy', optimizer=optimizer, metrics=['accuracy'])

    return model

