"""## Time Series Forecasting with ARIMA

We will now apply the ARIMA model to forecast the daily violent tweet counts. Before fitting the ARIMA model, we need to:

1. **Select the target variable**: We will use the `violent_tweet_count` column from the `daily_tweet_counts` DataFrame as our target variable.
2. **Check for stationarity**: ARIMA models require the time series data to be stationary. We will use statistical tests (e.g., Augmented Dickey-Fuller test) to check for stationarity and apply differencing if necessary.
3. **Determine the ARIMA order (p, d, q)**: We will use techniques like analyzing the Autocorrelation Function (ACF) and Partial Autocorrelation Function (PACF) plots or an auto-ARIMA function to find the optimal order for the model.
4. **Split the data**: Split the data into training and testing sets to evaluate the model's performance.
5. **Fit the ARIMA model**: Train the ARIMA model on the training data.
6. **Make predictions**: Generate forecasts on the test data.
7. **Evaluate the model**: Assess the performance of the ARIMA model using appropriate metrics.

### Check for Stationarity

ARIMA models require the time series data to be stationary. We will use the Augmented Dickey-Fuller test to check for stationarity.
"""

from statsmodels.tsa.stattools import adfuller

# Select the target variable
violent_tweets_ts = daily_tweet_counts['violent_tweet_count']

# Perform Augmented Dickey-Fuller test
result = adfuller(violent_tweets_ts)

print('ADF Statistic: %f' % result[0])
print('p-value: %f' % result[1])
print('Critical Values:')
for key, value in result[4].items():
    print('\t%s: %.3f' % (key, value))

# Interpret the results
if result[1] <= 0.05:
    print("\nThe time series is likely stationary.")
else:
    print("\nThe time series is likely non-stationary. Differencing may be required.")

"""### Determine ARIMA Order (p, d, q)

To determine the order of the ARIMA model (p, d, q), we will analyze the Autocorrelation Function (ACF) and Partial Autocorrelation Function (PACF) plots.

- **p**: The order of the autoregressive (AR) model. Look at the PACF plot to determine the number of AR terms.
- **d**: The degree of differencing. We already checked for stationarity and the data appears stationary, so d is likely 0.
- **q**: The order of the moving average (MA) model. Look at the ACF plot to determine the number of MA terms.
"""

from statsmodels.graphics.tsaplots import plot_acf, plot_pacf

# Plot ACF and PACF
