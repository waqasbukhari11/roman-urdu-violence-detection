"""### Interpreting ACF and PACF Plots and Fitting ARIMA

Based on the ACF and PACF plots:

- The **PACF** plot shows significant spikes at the initial lags, which then drop off. The lag at which the PACF cuts off can suggest the order of the AR component (p).
- The **ACF** plot shows a similar pattern, which can suggest the order of the MA component (q).

Interpreting these plots can be subjective. We can try a few different orders based on the visual cues or use an auto-ARIMA function to find the best order automatically. For now, let's proceed with splitting the data and fitting an ARIMA model with a chosen order (we can refine this later if needed).

We will split the data into a training set and a testing set. A common split is 80% for training and 20% for testing.
"""

